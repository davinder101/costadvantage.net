  <h3>What Is Dispute Resolution?</h3>                
<p>Dispute resolution is a term that refers to a number of processes that can be used to resolve a conflict, dispute or claim. Dispute resolution may also be referred to as alternative dispute resolution, appropriate dispute resolution, or ADR for short. Dispute resolution processes are alternatives to having a court. decide the dispute in a trial or other institutions decide the resolution of the case or contract. </p>                  

<p>Dispute resolution processes can be used to resolve any type of dispute including family, neighborhood, employment, business, housing, personal injury, consumer, and environmental disputes. </p>    



<h3>Why Use Dispute Resolution?</h3>
<p>Dispute resolution processes have several advantages. For instance, many dispute resolution processes are cheaper and faster than the traditional legal process. Certain processes can provide the parties involved with greater participation in reaching a solution, as well as more control over the outcome of the dispute. In addition, dispute resolution processes are less formal and have more flexible rules than the trial court.</p>              
                  

<h3>Do I Need an Attorney to Participate in Dispute Resolution?</h3>                  

<p>In many processes, you are not required to have an attorney to participate. In cases where the court or judge has referred the case to a dispute resolution process, attorneys often participate. The role of an attorney in a dispute resolution process varies depending upon the nature of the dispute and the type of dispute resolution process. In many dispute resolution processes, attorneys accompany their clients and participate either as counselors or as advocates.</p>                  
                  
